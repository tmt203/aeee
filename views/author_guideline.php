								<div class="sj-borderheading">
									<h3>Submission of Manuscripts</h3>
								</div>
								<div class="sj-description">
									<p>All articles should be submitted to the Advances in Electrical and Electronic Engineering via the Journal's online web-based submission system. All submissions sent to the Advances in Electrical and Electronic Engineering will be checked by <strong>Crossref Similarity Check (powered by iThenticate)</strong>. It is the authors´ responsibility to verify that all their files have been uploaded correctly.
									</p>The incoming articles are subject to:
									<ul>
										<li>two-round reviews.</li>
									</ul>
									<p>Prior to the review process all articles are subject to proper:
									</p>
									<ul>
										<li>plagiarism check by software tools.</li>
									</ul>
									<p>The software tools help to detect whether an article is plagiarism, or they can determine its percentage similarity with another article.
									</p>
									<p>Each author/team of authors are obliged to recommend:</p>
									<ul>
										<li>two suitable independent reviewers with at least Dr. or Ph.D. degrees,</li>
									</ul>
									<p>both with expertise in the field of the submitted article, and willing to review the article in question. The two reviewers must not be employed at the same workplace as the author/team of authors of the reviewed article, and the affiliations of both the reviewers have to differ as well. The authors have to declare that the selected reviewers are apprized of the fact that they may be asked for performing reviews in the future too and that they are automatically registered and listed in the reviewers´ file of the Advances in Electrical and Electronic Engineering journal. An author/team of authors can submit only two articles concurrently. In such a case there must be different reviewers for each of these articles. It is not possible to recommend the same reviewer for both the articles. The Editorial Staff also reserves the right not to assign an unsuitable reviewer/reviewers recommended by an author/team of authors of the article in question.
									</p>
									<p>When submitting an article to the OJS system of the Advances in Electrical and Electronic Engineering, the author/team of authors have to enter the following information into the “Comments for the Editor”: the reviewers’ full names (first name, middle name(s)/middle initial, last name), email addresses, affiliation or links on their web pages, and the field of expertise.
									</p>The Editorial Staff members will create reviewers’ accounts in the OJS system of the Advances in Electrical and Electronic Engineering and will send the login information to the two recommended reviewers.

									<p>In the first round the texts are presented in anonymous form to the two independent expert reviewers and, depending on their reviews, the articles can be passed over to the second round. The Editorial Team will afterwards check out all the other aspects of the articles submitted for publication in the Advances in Electrical and Electronic Engineering.
									</p>
									<p>The articles are only accepted in:
									</p>
									<ul>
										<li>the English language.</li>
									</ul>
									<p>The preferred formats for the articles are:</p>
									<ul>
										<li>doc,</li>
										<li>docx,</li>
										<li>rtf,</li>
										<li>odt,</li>
										<li><strong>PDF only if you write your article in LaTeX</strong> (the PDF file is uploaded as an article, but the LaTeX versions of your article including all figures and tables are uploaded as supplementary files).</li>
									</ul>
									<p><strong>
											<img title="ZIP" src="http://advances.uniza.sk/OJS/public/site/images/lat04/zipa.png" alt="ZIP" width="35" height="35" />
											<span style="text-decoration: underline;">
												<a href="/files/journals/1/AEEE-Template.zip" target="_self">Actual Template Version</a>
											</span>
										</strong></p>
									<p>Each article has to be sent in an A4 format
										<a href="http://www.cl.cam.ac.uk/~mgk25/iso-paper.html" target="_blank"><strong> </strong></a>A4 format (according to ISO 216, DIN 476 size 210×297 mm). The upper edge size has to be 31 mm and the lower edge size 28 mm. The right and left edges have to be 20 mm each. The sizes of headers and footers should be set at the standard 18 mm. The article should not exceed the range of 8 pages written in the required format in Microsoft Word using the main font Times New Roman. The details are given in the actual template for the authors. The article should contain:
									</p>
									<ul>
										<li>the title of the article (18pt font, bold, caps, centered),</li>
										<li>the names of the authors (12pt font, italics, large font last name (surname), all centered),</li>
										<li>the author´ workplace and its physical address (10pt regular font, centered),</li>
										<li>the email addresses of all authors (10pt regular font, centered),</li>
										<li>the abstract (10pt font, italics, with a maximum of 250 words), </li>
										<li>the keywords (10pt font, bold and in italics, at least 3 keywords), </li>
										<li>the article text (10pt regular font, single spacing) must be in a two-column layout (84.5 mm width of columns, the gap between the columns 6 mm) should include the introduction, theoretical part, experimental part, and the conclusion of the discussions,</li>
										<li>the literature used according to ISO 690 (based on a 8pt regular font, the reference name 8pt font, italics),</li>
										<li>about authors (10pt regular font, single spacing).</li>
									</ul>
									<p>The pictures, charts and diagrams listed in the article have to be sent as an attachment in a vector quality and format (PDF, SVG, EPS, etc.). If you do not have the vector format of your figures, you have to send the files with the values for plotting the graphs. We will prepare your graphs in the correct vector format. For more details in case of vector format: <strong> <a href="https://youtu.be/-Fs2t6P5AjY" target="_blank">Video animation</a></strong>.</p>
									<p>Other formats are not accepted. We primarily prefer delivering of all pictures, charts and diagrams in a supplementary file in a vector format on account of the transcription of the article into the LaTeX template version. The designation and the description of the images has to be in accord with the template for the authors.</p>
									<p>The mathematical relationships and equations have to be written in the Equation Editor in Microsoft Word using the Times New Roman font (10pt regular font) and the Symbol font for the symbols (10pt font).</p>
									<p>The tables have to be included in the text of the article. The designation and description of the tables accord with the templates determined for the authors.<br /><br />The images and tables written in different languages than the article itself have to contain bilingual descriptions.<br /><br />The literature used in the article has to be written according to <strong>
											<a href="http://paedpsych.jk.uni-linz.ac.at/internet/ARBEITSBLAETTERORD/LITERATURORD/ZitationISO690.html#5.1" target="_blank">ISO 690</a></strong> (translation of the international standards). The citations in the text have to be given in square brackets, and the references summarizing the sources used have to be listed chronologically (i.e. in the order in which they were used) at the end of the articles.
									</p>
								</div>
