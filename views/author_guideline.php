									<h3>Concurrent Submissions</h3>
								</div>
								<div class="sj-description">
									<p>To afford a great number of authors/teams of authors opportunities to publish in the Journal and to ensure a great diversity of opinions, each writer is limited to submitting only two articles to a review process concurrently. If authors/teams of authors have exceeded the limit and have simultaneously sent off more articles to the Journal, they will need to wait until the reviewing process of at least one of their submitted articles has been completed before submitting another article for consideration.
									</p>
