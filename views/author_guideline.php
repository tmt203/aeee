									<h3>Authors' Contributions</h3>
								</div>
								<div class="sj-description">
									<p>According to the requirements for Manuscripts Submitted to Advances in Electrical and Electronic Engineering journal (AEEE), designation as an author must satisfy four conditions. The author must have:
									<ul>
										<li>1. Contributed substantially to the conception and design of the study, the acquisition of data, or the analysis and interpretation.</li>
									</ul>
									<ul>
										<li>2. Drafted or provided critical revision of the article.</li>
									</ul>
									<ul>
										<li>3. Provided final approval of the version to publish.</li>
									</ul>
									<ul>
										<li>4. Agreed to be accountable for all aspects of the work in ensuring that questions related to the accuracy or integrity of any part of the work are appropriately investigated and resolved.</li>
									</ul>
									For this reason, we would like to ask authors to specify the contribution of each author of your manuscript. For each author of your manuscript, please indicate the types of contributions the author has made from the table of contributions below. An author may have made more than one type of contribution. Optionally, for each contribution type, you may specify the contribution of an author in more detail by providing a one-sentence statement in which the contribution is summarized. If an author has made a contribution that is not covered in our table of contributions, then please choose ‘Other contribution’ and provide a one-sentence statement summarizing the author’s contribution.
