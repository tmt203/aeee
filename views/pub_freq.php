								<h2>Publication Frequency</h2>
								<div class="sj-description">
									<p>Articles for publication in the Advances in Electrical and Electronic Engineering are released quarterly in the spring, summer, fall, and winter issues. The periodical is published four times a year in electronic on-line version, as a rule on:</p>
									<ul>
										<li> March 31, </li>
										<li>June 30, </li>
										<li>September 30,</li>
										<li>December 31. </li>
									</ul>
									<p>Regular article submissions are accepted throughout the year. The period of time between submitting an article and its publication is about:<strong> </strong></p>
									<ul>
										<li>5-10 months. </li>
									</ul>
									<p>In the meantime, every article undergoes the review process and the necessary check-ups. As soon as all the required arrangements have been made, the author(s) and co-author(s) get a message via the messaging system or by the Editor´s email saying that the article has been accepted for publication. If need be, the author(s) and co-author(s) are also informed either via the messaging system or by the Editor´s email about all inadequacies and the reasons for the necessary corrections of the article, or for its rejection.</p>
								</div>
							</div>
							<div class="sj-ourhistory sj-sectioninnerspace">
								<div class="sj-borderheading">
									<h2>Special Issues from Conference</h2>
								</div>
								<div class="sj-description">
									<p>Our Journal does not publish any conference materials (e.g. papers submitted to conferences), with the possible exceptions of the invited keynote speakers, or some specially selected papers. It means that only papers of sufficiently good scientific quality will be accepted. However, the extended versions of full papers, different from those presented at conferences, can be published. A paper submitted to this Journal should differ from the previously published material by at least<strong>:</strong></p>
									<ul>
										<li> 40 percent. </li>
									</ul>
									<p>The Journal Editor´s Office requires the conference proceedings/articles, which are sent for publication in a special issue and the extension of which is to be evaluated, to be delivered on CDs well in advance. The decision about the acceptance or the rejection of the material depends on the recommendations of:</p>
									<ul>
										<li>two independent reviewers,</li>
									</ul>
									<p>and, on top of that, only in the event that:</p>
									<ul>
										<li>one of them is appointed by the given Conference Scientific Committee, and,</li>
										<li>the second one by the Journal Editorial Board. </li>
									</ul>
									<p>In questionable cases, the Editorial Board may reject papers recommended by a third referee (third party). The papers from each individual conference will be published together in a special issue of the Advances in Electrical and Electronic Engineering journal.</p>
